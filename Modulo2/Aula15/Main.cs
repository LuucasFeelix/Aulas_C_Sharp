using System;

class Aula15{
    static void Main(){
        //Operadores Relacionas

        int n1 = 5;
        int n2 = 10;

        bool v1 = n1 == n2; //Igualdade(Se sao iguais)
        bool v2 = n1 != n2; //Diferença(Se são diferentes)
        bool v3 = n1 > n2; // Maior que
        bool v4 = n1 < n2; // Menor que
        bool v5 = n1 >= n2; // Igual ou maior que
        bool v6 = n1 <= n2; // Igual ou menor que
        Console.WriteLine(v1);
        Console.WriteLine(v2);
        Console.WriteLine(v3);
        Console.WriteLine(v4);
        Console.WriteLine(v5);
        Console.WriteLine(v6);
    }
    
}